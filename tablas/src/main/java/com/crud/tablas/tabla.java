    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed

        // TODO add your handling code here:
        String campo = txtCodigo.getText();
        String where = "";
        
        if (!"".equals(campo)){
            where = "WHERE codigo='"+ campo +"'";
        }
        
        try{
            DefaultTableModel modelo = new DefaultTableModel();
            tableProductos.setModel(modelo);
            
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConnection();
            
            String sql = "SELECT codigo, nombre, precio, cantidad FROM producto " + where;
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            ResultSetMetaData rsMD = rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount();
            
            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre");
            modelo.addColumn("Precio");
            modelo.addColumn("Cantidad");
            
            int[] anchos = {50,200,50,50};
            
            for(int x = 0; x < cantidadColumnas; i++){
                tableProductos.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);
            }
            
            while(rs.next()){
                Object [] filas = new Object[cantidadColumnas];
                for(int i=0; i < cantidadColumnas; i++){
                    filas[i] = rs.getObject(i+1);
                }
                modelo.addRow(filas);
            }
            
        } catch (Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btnCargarActionPerformed

